import turtle
turtle.shape('turtle')
finn=turtle.clone()
finn.shape('square')
finn.goto(100,0)
finn.goto(100,100)
finn.goto(0,100)
finn.goto(0,0)
charlie=turtle.clone()
charlie.shape('triangle')
charlie.goto(100,0)
charlie.goto(50,100)
charlie.goto(0,0)
finn.left(-180)
finn.forward(100)
finn.stamp()
finn.goto(0,0)
finn.stamp()
finn.goto(250,250)
finn.clearstamps()
turtle.mainloop()
